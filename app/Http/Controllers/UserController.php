     */
    public function store(Request $request)
    {
        $newUser = new User;
        $newUser->name = $request->user["name"];
        $newUser->email = $request->user["email"];
        $newUser->password = $request->user["password"];
        $newUser->saldo = 0.00;
        $newUser->totaal = 0.00;
        $newUser->save();

        return $newUser;
    }

